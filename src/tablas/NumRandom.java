package tablas;

public class NumRandom {
    int i;
    String num;

    public NumRandom() {
    }

    public NumRandom(int i, String num) {
        this.i = i;
        this.num = num;
    }

    public int getI() {
        return this.i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public String getNum() {
        return this.num;
    }

    public void setNum(String num) {
        this.num = num;
    }
}

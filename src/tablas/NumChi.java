package tablas;

public class NumChi {
    String inter;
    String fo;
    String fe;
    String c;

    public NumChi(String inter, String fo, String fe, String c) {
        this.inter = inter;
        this.fo = fo;
        this.fe = fe;
        this.c = c;
    }

    public String getInter() {
        return inter;
    }

    public void setInter(String inter) {
        this.inter = inter;
    }

    public String getFo() {
        return fo;
    }

    public void setFo(String fo) {
        this.fo = fo;
    }

    public String getFe() {
        return fe;
    }

    public void setFe(String fe) {
        this.fe = fe;
    }

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }
}
